const hide_show1 = document.querySelector('#hide_show1');
let is_clicked1 = true;

let show_or_hide1 = function(){
    if(is_clicked1){
        hide_show1.style.display = 'block';
        is_clicked1 = false;
    }else{
        hide_show1.style.display = 'none';
        is_clicked1 = true;
    }
}

